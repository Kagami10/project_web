	<h>Perkembangan jaman yang kian pesat membuat dunia otomotif terus berinovasi. Termasuk kalangan kaum muda terus berlomba mencetak karya inovasi yang unik serta kreatif yang akhirnya sanggup menjadikan sebagai sebuah hobi. Nah, dari inovasi tersebut, terdapat komunitas unik yang baru-baru ini mencuri banyak perhatian masyarakat Indonesia, namanya adalah Kustom Battle</h>
	<br>
	<h>Kustom Battle merupakan sebuah komunitas diecast atau model scale miniatur mobil yang bernama AOCI atau All Out Custom Indonesia. Anggota dari komunitas ini telah mencapai 28 ribu personel dan separuhnya dari luar negeri.

    <h>Penggagas sekaligus Penyelenggara Acara, Yuyu menjelaskan bahwa kegiatan komunitas ini adalah kustom untuk skala model. Skala model ini artinya merupakan skala kecil yakni sebuah miniatur mobil. "Untuk perbandingan skala miniatur ini kurang lebih sekitar 1 banding 64," jelas Yuyu kepada TIMES Indonesia, Selasa (30/11/2021).</h>
    </br>
	<h>Ia menambahkan, kegiatan komunitas ini tergolong cukup baru di Indonesia. Sehingga, rangkaian acara tersebut pun telah digelar di berbagai kota salah satunya Yogyakarta. Karena ini merupakan sebuah kustom yang baru dan unik, maka rangkaian kegiatan dilaksanakan di lokasi.</h>

    <h>"Kegiatan ini tergolong cukup baru sehingga kita adakan di lokasi. Untuk peraturan yang digunakan, ada tantangan untuk mereka selama 10 jam waktu untuk merangkai miniatur mobil-mobilnya," katanya.</h>

    <h>Selain itu, para peserta yang mengikuti ajang kegiatan ini sekitar ada 20 orang. Masing-masing berasal dari Jawa Timur, Yogyakarta dan juga Jakarta. Dalam hal ini, road show untuk Kustom Battle diselenggarakan di kota Yogyakarta.</h>

    <h>"Ini merupakan hari ketiga kita di Yogyakarta. Sebelumnya, sudah diadakan di Bali dan Surabaya. Untuk bulan Desember mendatang, akan diadakan di Jakarta," ujar Yuyu.</h>
